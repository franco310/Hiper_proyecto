    private void btnBuscarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProductoActionPerformed



   IProducto productoDao = new ProductoImpl();
        try {
            limpiarControles();
            producto = productoDao.obtener(
                    Integer.parseInt(txtBuscarCodigo.getText()));
            if (producto != null) {
                txtCodigo.setText(Integer.toString(producto.getIdProducto()));
                txtNombre.setText(producto.getNombre());
                txtPrecio.setText(Integer.toString(producto.getPrecio()));
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar el curso!!",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
   






    }//GEN-LAST:event_btnBuscarProductoActionPerformed
